#include <stdio.h>

/**
 * Displays "Hello world!" then exits with success.
 *
 * @param argc
 *        The count of shell parameters passed to this function
 * @param argv
 *        An array of all the shell args passed to this function
 */
int main(int argc, char **argv) {
  printf("Hello world!\n");
  return 0;
}
